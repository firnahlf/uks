<div class="jumbotron">
    <p><h4>
        <strong>Usaha Kesehatan Sekolah</strong> disingkat UKS adalah program pemerintah untuk meningkatkan pelayanan kesehatan, pendidikan kesehatan dan pembinaan lingkungan sekolah sehat atau kemampuan hidup sehat bagi warga sekolah. Melalui program UKS diharapkan dapat meningkatkan pertumbuhan dan perkembangan peserta didik yang harmonis dan optimal, agar menjadi sumber daya manusia yang berkualitas.
    </h4></p>
    <p><h4>
        Berdasarkan buku Panduan Pendidikan Dasar dan Terapan (DIKSANTER) Trias UKS Kelompok Studi dan Ekstrakurikuler (KSE) Jurnalisme Adiwiyata Bermitra (JUWITER), landasan hukum penyelenggaraan UKS diantaranya :
    </h4></p>
    
    <ol>
            <li>Undang - undang Nomor 20 Tahun 2003 tentang Sistem Pendidikan Nasional</li>
            <li>Undang-Undang Nomor 36 Tahun 2009 tentang Kesehatan</li>
            <li>Surat Keputusan Bersama Mendiknas, Menkes, Menag dan Mendagri Nomor : 2/P/SKB/2003, Nomor : MA/230B/2003, Nomor : 445-404 Tahun 2003 Tanggal 23 Juli 2003 Tentang Tim Pembina Usaha Kesehatan Sekolah</li>
    </ol>
    <br>
    <h3><strong>Inovasi Pengembangan UKS (Usaha Kesehatan Sekolah)</strong></h3>
    <p><h4>
        Tuntunan program UKS seperti program Kemenkes RI terkait UKS yang tercetus sejak 1956 sampai sekarang sesungguhnya menjadi tuntunan program yang harus diterapkan oleh semua sekolah. Bukan hanya sekolah, peran tim pembina UKS dan Penanggungjawab UKS di masing-masing Puskesmas juga diharapkan untuk aktif melaksanakan pembinaan.
    </h4></p>
    
    <p><h4>
        Kondisi kekinian Program UKS seringkali aktif hanya menjelang lomba tahunan yang diadakan berdasarkan landasan hukum diatas. Berdasarkan hal tersebut, lahirnya JUWITER yang merupakan salah satu kelompok studi dan ekstrakurikuler yang mengembangkan Trias UKS sebagai salah satu dari 13 program prioritas menjadi salah satu inovasi yang ikut serta memacu terepan pelaksanaan UKS dikatakan penting untuk didukung.
    </h4></p>
    <p><h4>
        Pengembangan Program Trias UKS yang merupakan salah satu program KSE JUWITER tersebut, selain dipacu melalui program pengelolaan majalah dinding dan buletin sekolah, maka untuk mendukung pelaksanaannya dilaksananakan juga pelatihan Kader Kesehatan Remaja (KRR) kerjasama dengan Puskesmas yang sesungguhnya unsur pembina UKS tingkat kecamatan berasal atau ada di setiap Puskesmas.
    </h4></p>
